class MedievalLine {
    constructor(x, y) {
        this.x = x;
        this.y = y;
        this.opacity = 1;
        this.decay = 0.032;
        this.segments = this.createSegments();
    }

    update() {
        this.opacity -= this.decay;
    }

    createSegments() {
        const segments = [];
        const branchCount = Math.floor(Math.random() * 3) + 4;
        const baseRotation = Math.random() * Math.PI * 2;

        for (let i = 0; i < branchCount; i++) {
            const angle = baseRotation + (Math.PI * 2 * i) / branchCount + (Math.random() - 0.5) * 0.4;
            this.buildBranch({ x: 0, y: 0 }, angle, 0, segments);
        }

        return segments;
    }

    buildBranch(startPoint, angle, depth, segments) {
        const steps = depth === 0 ? Math.floor(Math.random() * 3) + 4 : Math.floor(Math.random() * 2) + 2;
        let currentPoint = { ...startPoint };
        let currentAngle = angle;

        for (let step = 0; step < steps; step++) {
            currentAngle += (Math.random() - 0.5) * (depth === 0 ? 0.48 : 0.66);
            const segmentLength = (depth === 0 ? 16 : 10) + Math.random() * (depth === 0 ? 22 : 12);
            const nextPoint = {
                x: currentPoint.x + Math.cos(currentAngle) * segmentLength,
                y: currentPoint.y + Math.sin(currentAngle) * segmentLength
            };

            segments.push({
                from: currentPoint,
                to: nextPoint,
                width: Math.max(0.7, 1.6 - depth * 0.35)
            });

            if (depth < 2 && Math.random() < (depth === 0 ? 0.42 : 0.2)) {
                const branchAngle = currentAngle + (Math.random() > 0.5 ? 1 : -1) * (0.35 + Math.random() * 0.55);
                this.buildBranch(nextPoint, branchAngle, depth + 1, segments);
            }

            currentPoint = nextPoint;
        }
    }

    draw() {
        ctx.save();
        ctx.translate(this.x, this.y);
        ctx.lineCap = 'round';
        ctx.lineJoin = 'round';

        this.segments.forEach(segment => {
            ctx.globalAlpha = this.opacity * 0.16;
            ctx.strokeStyle = 'rgba(176, 146, 255, 0.58)';
            ctx.lineWidth = segment.width * 2.8;
            ctx.beginPath();
            ctx.moveTo(segment.from.x, segment.from.y);
            ctx.lineTo(segment.to.x, segment.to.y);
            ctx.stroke();

            ctx.globalAlpha = this.opacity * 0.82;
            ctx.strokeStyle = 'rgba(212, 196, 255, 0.82)';
            ctx.lineWidth = segment.width;
            ctx.beginPath();
            ctx.moveTo(segment.from.x, segment.from.y);
            ctx.lineTo(segment.to.x, segment.to.y);
            ctx.stroke();
        });

        ctx.globalAlpha = this.opacity * 0.45;
        ctx.fillStyle = 'rgba(214, 198, 255, 0.72)';
        ctx.beginPath();
        ctx.arc(0, 0, 2.4, 0, Math.PI * 2);
        ctx.fill();

        ctx.restore();
    }
}

let lastTime = 0;
const throttle = 80;
let lastTheme = '';

function animate(currentTime) {
    const activeTheme = document.body.getAttribute('data-active-theme');
    
    if (activeTheme !== lastTheme) {
        lines = [];
        lastTheme = activeTheme;
    }
    
    if (currentTime - lastTime > throttle) {
        ctx.clearRect(0, 0, canvas.width, canvas.height);
        
        if (activeTheme === 'medieval') {
            const isMouseIdle = currentTime - lastMouseMoveTime > 140;
            const spawnChance = isMouseIdle ? 0.014 : 0.16;

            if (Math.random() < spawnChance) {
                lines.push(new MedievalLine(mouseX, mouseY));
            }
            
            lines = lines.filter(line => {
                line.update();
                line.draw();
                return line.opacity > 0;
            });
        }
        
        lastTime = currentTime;
    }
    
    requestAnimationFrame(animate);
}

animate(0);


function getDefaultLanguage() {
    const savedLang = localStorage.getItem('alemisol-lang');
    if (savedLang === 'sr' || savedLang === 'en') return savedLang;
    
    const browserLang = navigator.language || navigator.userLanguage;
    const langCode = browserLang.toLowerCase().split('-')[0];
    
    if (langCode === 'sr' || langCode === 'hr' || langCode === 'bs') return 'sr';
    if (langCode === 'en') return 'en';
    
    return 'sr';
}

let currentLang = getDefaultLanguage();

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('alemisol-lang', lang);
    document.documentElement.lang = lang;
    document.body.setAttribute('data-lang', lang);
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const keys = key.split('.');
        let value = translations[lang];
        
        for (const k of keys) {
            if (!value || typeof value !== 'object') { value = undefined; break; }
            value = value[k];
        }
        
        if (value && typeof value === 'string') {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = value;
            } else if (key === 'hero.title') {
                const lines = String(value)
                    .split('|')
                    .map(line => line.trim())
                    .filter(Boolean);
                el.innerHTML = lines.map(line => `<span class="hero-title-line">${line}</span>`).join('');
            } else {
                el.textContent = value;
            }
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        const keys = key.split('.');
        let value = translations[lang];

        for (const k of keys) {
            if (!value || typeof value !== 'object') { value = undefined; break; }
            value = value[k];
        }

        if (typeof value === 'string') {
            el.placeholder = value;
        }
    });
    
    document.querySelectorAll('.lang-option').forEach(option => {
        option.classList.toggle('active', option.getAttribute('data-lang') === lang);
    });
}


const contactForm = document.getElementById('contactForm');
const contactFormStatus = document.getElementById('contactFormStatus');
const CONTACT_EMAIL = 'Alemisol@gmail.com';

function setContactFormStatus(message, type = '') {
    if (!contactFormStatus) return;
    contactFormStatus.textContent = message;
    contactFormStatus.classList.remove('is-success', 'is-error');
    if (type) {
        contactFormStatus.classList.add(type);
    }
}

function clearContactFieldErrors() {
    if (!contactForm) return;
    contactForm.querySelectorAll('input, textarea').forEach(field => {
        field.removeAttribute('aria-invalid');
    });
}

function markFieldInvalid(fieldId) {
    const field = document.getElementById(fieldId);
    if (field) {
        field.setAttribute('aria-invalid', 'true');
        field.focus();
    }
}

function validateContactPayload({ name, email, phone, project }) {
    const validationText = translations[currentLang].contact.form;

    if (name.length < 2) {
        markFieldInvalid('name');
        return validationText.validationName;
    }

    if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
        markFieldInvalid('email');
        return validationText.validationEmail;
    }

    if (phone && phone.replace(/[^\d+]/g, '').length < 6) {
        markFieldInvalid('phone');
        return validationText.validationPhone;
    }

    if (!project.length) {
        markFieldInvalid('project');
        return validationText.validationProject;
    }

    return '';
}

if (contactForm) {
    contactForm.querySelectorAll('input, textarea').forEach(field => {
        field.addEventListener('input', () => {
            field.removeAttribute('aria-invalid');
            if (contactFormStatus?.textContent) {
                setContactFormStatus('');
            }
        });
    });

    contactForm.addEventListener('submit', async (e) => {
        e.preventDefault();
        
        const submitBtn = contactForm.querySelector('.btn-primary');
        const originalText = translations[currentLang].contact.form.submit;
        const formData = new FormData(contactForm);
        const payload = {
            name: formData.get('name')?.toString().trim() || '',
            email: formData.get('email')?.toString().trim() || '',
            phone: formData.get('phone')?.toString().trim() || '',
            project: formData.get('project')?.toString().trim() || ''
        };

        clearContactFieldErrors();
        const validationError = validateContactPayload(payload);
        if (validationError) {
            setContactFormStatus(validationError, 'is-error');
            return;
        }
        
        submitBtn.textContent = translations[currentLang].contact.form.sending;
        submitBtn.disabled = true;
        setContactFormStatus('');
        
        try {
            const response = await fetch(`https://formsubmit.co/ajax/${CONTACT_EMAIL}`, {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                    Accept: 'application/json'
                },
                body: JSON.stringify({
                    _subject: `Alemisol upit — ${payload.name}`,
                    _replyto: payload.email,
                    _captcha: 'false',
                    name: payload.name,
                    email: payload.email,
                    phone: payload.phone || '-',
                    message: payload.project
                })
            });

            const result = await response.json().catch(() => null);

            if (!response.ok || (result && result.success === false)) {
                throw new Error(result?.message || 'Request failed');
            }

            submitBtn.textContent = translations[currentLang].contact.form.sent;
            setContactFormStatus(translations[currentLang].contact.form.success, 'is-success');
            contactForm.reset();
        } catch (error) {
            const errorMessage = error instanceof Error ? error.message.toLowerCase() : '';
            const activationIssue = errorMessage.includes('activate') || errorMessage.includes('confirm');

            if (activationIssue) {
                setContactFormStatus(translations[currentLang].contact.form.activation, 'is-error');
            } else {
                setContactFormStatus(translations[currentLang].contact.form.error, 'is-error');
            }
        } finally {
            setTimeout(() => {
                submitBtn.textContent = originalText;
                submitBtn.disabled = false;
            }, 1600);
        }
    });
}
